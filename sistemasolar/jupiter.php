<p><br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Es el planeta más grande del sistema solar 
y quinto en cercanía al sol. Su nombre es en honor al dios supremo romano Júpiter, 
Zeus en la mitología griega.
Es una gran bola de gas. Esta formado fundamentalmente por hidrógeno y helio. 
Una de la característica más relevante es su gran mancha roja, que es un enorme 
anticiclón con vientos de hasta 500 Km/h. El tamaño de la mancha es dos veces y 
media que la de la tierra. Júpiter tiene la velocidad de rotación más rápida 
de los planetas del Sistema Solar, tarda poco menos de 10 horas en girar sobre su eje.
La atmósfera esta dividida en cinturones oscuros llamados bandas y regiones claras 
llamadas zonas. Júpiter posee un tenue sistema de anillos.<br>
Júpiter tiene 67 satélites conocidos.
<p><b>Satélites galileano:</b>
Llamados así por ser descubiertos por Galileo Galilei el 7 de enero de 1610.
<p><b>Io:</b> El más cercano a júpiter y con una superficie volcánica.
<br>- Diámetro: 3.643 Km
<br>- Periodo orbital: 1,769138 días.
<p><b>Europa:</b> Es un mundo helado
<br>- Diámetro: 3.122 Km
<br>- Periodo orbital: 3,551181 días
<p><b>Gamínides:</b> Es el satélite más grande de todo el Sistema Solar. Esta compuesto de un núcleo de hierro, con un manto rocoso y de hielo.
<br>- Diámetro: 5.262 Km
<br>- Periodo orbital: 7,154553 días</p>
<p><b>Calisto:</b> Presenta cientos de cráteres de impactos.
<br>- Diámetro: 4.821 Km
<br>- Periodo orbital: 16,68902 días</p>
<p>Además existen unos 63 satélites menores que sumado a los 4 galileanos componen 67 satélites conocidos.</p>
<p>Los menores de divide en:
<br>- Grupo de Almatea: Son cuatro que giran en orbitas entre Júpiter y los satélites galileanos. Son Metis, Adrastea, Amaltea y Tebe.
<p>- Satélites regulares: Orbitan muy lejanos a Júpiter. Son 59 conocidos Algunos son Aedea, Aitre, Arce, etc..</p>
<p>También se conocen cientos de asteroides troyanos, el mayor es el 624 Hector.
