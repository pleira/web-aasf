<p><br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nuestro Sistema Solar se encuentra dentro de una galaxia espiral. 
Esta galaxia nació hace 10.000 millones de años luz, mide unos 100.000 años luz de extremo a extremo y posee más de 200.000 millones de estrellas. Si alzamos la vista hacia el cielo en una noche alejados de la contaminación lumínica de nuestra ciudades podemos observar una franja blanquecina compuesta por millones de estrellas. Los griegos creían que era la leche que esparcía la madre de los dioses, la denominaron camino de leche “Via Lactea”. Durante siglos todas las culturas hicieron sus conjeturas de lo que podía ser esa franja blanquecina. Hasta que en el siglo XVII Galileo la observó con su telescopio, constatando que se trataba de millones de estrellas.  
Nuestro Sistema Solar se encuentra en el denominado Brazo de Orión de la Vía Lactea a unos 25.000 años luz del centro.
<img id="imagen1" src="img_sistemasolar/vialactea.png">

<p><br>El Sistema Solar esta compuesto por 8 planetas y un gran número de objetos menores; planetas enanos, asteroides, cometas, meteroides, etc..
Los 8 planetas se dividen en dos grandes grupos según su posición dentro del sistemas solar con referencia al cinturón de asteroides y a su composición:

Planetas rocosos o interiores: Mercurio, Venus, La Tierra y Marte.
Planetas gaseoso o exteriores: Jupiter, Saturno, Urano y Neptuno.
<img id="imagen1" src="img_sistemasolar/sistemasolar.gif">
Entre la órbita de Marte y Jupiter se encuentra el Cinturón de Asteorides, compuesto por miles de ellos. También se encuentra un planeta enano reconocido por la UAI, “Ceres”.

Más allá del planeta Neptuno se encuentra el Cinturón de Kuiper donde tenemos 5 planetas enanos  reconocido oficialmente por UAI; Plutón, Eris, Makemake y Haumea. Existen muchos más pero aun no estan reconocido por la UAI (Orcus, Huya, Ixion, Varuma, Quaoar y Sedna. Mas alla se encuentra ella nuve de Oort, de donde proviene los cometas de largo recorrido.
Y en el centro de todos esto nuestra estrella el “Sol”.

