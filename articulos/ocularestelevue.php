	<p id="textoarticulos">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp

<br>Como algunos sabréis, hace algún tiempo decidí renovar mi serie de oculares con 
los famosos Ethos de la firma norteamericana Tele Vue. Concretamente he adquirido 
el 13 y el 8 mm, y estoy a la espera de que salga definitivamente al mercado el de 
21mm, que parece ser lo hará allá por el mes de diciembre. También compré, para el 
rango de altos aumentos, un Tele Vue Nagler zoom de 6 – 3 mm.
<img id="imagentelevue" src="img_articulos/ocularestelevue.jpg" width="50%" height="50%" border="1">
<br>Fue en la quedada de aficionados que tuvo lugar a comienzos de este año en Navas de 
Estena (Ciudad Real), organizada por la gente de la Asociación Astronómica Hubble, 
donde tuve la oportunidad, por primera vez, de tomar contacto con un Ethos. Se trataba 
de un 13 mm, el primero de la serie que Tele Vue sacó al mercado, y que se encontraba 
montado en un <b>Meade LightBridge de 12”</b>. Evidentemente, lo primero que me chocó fue el 
inmenso campo que se abría ante mis ojos, 100º de campo aparente con una extracción 
pupilar de 15 mm, se asemejaba a abrir una ventana al Espacio y fue, sin ninguna duda, 
amor a primera vista. Desde ese instante, tuve claro cuáles serían mis próximos oculares. 
En mi reflector, un <b>Orion Optics UK de 250 mm a f 4’8</b>, el Ethos de 13 mm orfrece 
un campo real de algo más de un grado a 93X, el de 8 mm, 0,66º para unos aumentos 
de 150X, y el de 21 mm dará 57X y 1’75º.
<br>Todos estos oculares, excepto el de 17 y el de 21 son híbridos, es decir, aceptan 
portaoculares de 2” y 1 ¼”. El rendimiento óptico, teniendo en cuenta el número de 
lentes que montan es sencillamente espectacular, con una corrección de aberraciones 
en todo el campo por encima de lo que me esperaba, ofreciendo imágenes bien corregidas,
 nítidas y contrastadas. No adolecen de “blackout” (al menos yo no lo aprecio) y los 
 considero muy cómodos de usar. Comparados con mis anteriores Vixen LVW (actualmente 
 en manos de José Antonio Mateo), a los que ya de por sí considero unos oculares muy 
 buenos, los Ethos ofrecen mayor calidad óptica general con 35º más de campo aparente. 
 Las vistas de los grandes cúmulos y nebulosas a través de estos oculares son 
 espectaculares y a día de hoy, probablemente no haya en el mercado ningún otro 
 ocular “Widefield” que le haga sombra. Siempre hay un pero, y es que los los 
 <b>Tele Vue Ethos son oculares realmente caros</b>, en España diríamos que rayan lo 
 prohibitivo, 570 euros para el de 13 mm y 550 euros para el de 8 mm, los colocan 
 en un segmento ciertamente al alcance de una minoría.
<br>Del <b>Nagler zoom de 6 -3 mm</b> sólo puedo hablar maravillas, es una pequeña joya que 
ofrece 4 focales en un solo ocular, a saber, 6, 5, 4 y 3mm. Con un campo aparente 
de 50 º y un contraste y nitidez excepcional, lo convierten en un ocular ideal para 
trabajos de alta resolución. Es extremadamente cómodo de usar, teniendo en cuenta las 
focales con las que nos movemos, y su sistema “click Stop” trabaja con una suavidad y
 precisión exquisitas. Su calidad óptica es similar a la que me ofrecía mi anterior 
 ocular “premium”, un Pentax Ortho XO de 5mm, o la de los orthos Kasai, con la 
 diferencia de que el zoom presenta mayor campo aparente y también mayor extracción 
 pupilar que éstos últimos. En cuanto al precio, alrededor de los 345 euros, pero en 
 este caso hay que tener en cuenta de que sustituimos a 4 “oculares”, más las focales 
 intermedias.
<br>En la foto se aprecian, de izquierda a derecha, el Nagler zoom y los Ethos de 8 y 13mm.
 Queda un hueco que espero, como ya he comentado con anterioridad, lo ocupe en un 
 futuro el Ethos de 21 mm. Un saludo.


